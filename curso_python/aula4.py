# tipos int e float
# int -> número inteiro
# o tipo int representa qualquer número
# o positivo ou negativo. int sem sinal é considerado
# positivo
print(11) # é inteiroS
print(-11) # é inteiro
print(0) #é inteiro

#float -> número com ponto flutuante (casas decimais)
# o tipo float representa qualquer número
# positivo ou negativo 
# float sem sinal é considerado positivo
print(-1.3, 0.0)
print(9.0, 7.8)

# a função type mostra o tipo que o Python
# inferiu ao valor
print(type("Fernanda Marques"))
print(type("-1"))
print(type(1.1), type(-1.1), type(0))